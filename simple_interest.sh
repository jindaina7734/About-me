Simple interest is a method of calculating interest that only takes into account the principal amount and the interest rate. The formula for simple interest is:

Code snippet
SI = P x R x T
Use code with caution. Learn more
Where:

SI = Simple interest
P = Principal amount
R = Interest rate
T = Time period
For example, if you borrow $100 at an interest rate of 5% for 1 year, you will pay $5 in simple interest.
